// Sử dụng hàm Math.abs()
let abs1 = Math.abs(-5.5);
let abs2 = Math.abs(10);

console.log(abc1); // 5.5
console.log(abs2); // 10
// Hàm Math.abs() trả về giá trị tuyệt đối của số truyền vào. Đối với -5.5, kết quả là 5.5. Đối với 10, kết quả vẫn là 10.

// Sử dụng hàm Math.ceil()
let ceil1 = Math.ceil(4.4);
let ceil2 = Math.ceil(5.8);

console.log(ceil1); // 5
console.log(ceil2); // 6
// Hàm Math.ceil() làm tròn số lên đến số nguyên gần nhất. Đối với 4.4, kết quả là 5. Đối với 5.8, kết quả là 6.

// Sử dụng hàm Math.floor()
let floor1 = Math.floor(5.7);
let floor2 = Math.floor(6.1);

console.log(floor1); // 5
console.log(floor2); // 6
// Hàm Math.floor() làm tròn số xuống đến số nguyên gần nhất. Đối với 5.7, kết quả là 5. Đối với 6.1, kết quả là 6.

// Sử dụng hàm Math.round()
let round1 = Math.round(10.1);
let round2 = Math.round(20.6);

console.log(round1); // 10
console.log(round2); // 21
// Hàm Math.round() làm tròn số đến số nguyên gần nhất. Đối với 10.1, kết quả là 10 (vì 10.1 gần 10 hơn 11). Đối với 20.6, kết quả là 21 (vì 20.6 gần 21 hơn 20).