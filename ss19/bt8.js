let month = Number(prompt("Mời bạn nhập tháng"));
if (month == 1 || month == 2 || month == 3) {
    alert(`Tháng ${month} bạn vừa nhập là mùa xuân`);
} else if (month == 4 || month == 5 || month == 6) {
    alert(`Tháng ${month} bạn vừa nhập là mùa hạ`);
} else if (month == 7 || month == 8 || month == 9) {
    alert(`Tháng ${month} bạn vừa nhập là mùa thu`);
} else if (month == 10 || month == 11 || month == 12) {
    alert(`Tháng ${month} bạn vừa nhập là mùa đông`);
} 